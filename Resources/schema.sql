CREATE TABLE IF NOT EXISTS ACCIDENTS(
    ID TEXT PRIMARY KEY,
    SOURCE TEXT NOT NULL,
    SEVERITY INTEGER NOT NULL,
    START_TIME TEXT NOT NULL,
    STREET TEXT NOT NULL,
    SIDE TEXT NOT NULL,
    CITY  TEXT,
    COUNTY  TEXT NOT NULL,
    STATE  TEXT NOT NULL,
    ZIPCODE TEXT ,
    TIMEZONE TEXT ,
    TEMPERATURE REAL ,
    WIND_CHILL REAL,
    HUMIDITY REAL,
    VISIBILITY REAL,
    WIND_SPEED REAL ,
    WEATHER_CONDITION TEXT,
    SUNRISE_SUNSET TEXT
);